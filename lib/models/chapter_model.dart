/// A single `term` / `definition` pair.
class DefinitionEntry {
  final String term;
  final String definition;
  DefinitionEntry({required this.term, required this.definition});

  factory DefinitionEntry.fromJson(Map<String, dynamic> json) {
    return DefinitionEntry(
      term: json['term']?.toString() ?? '',
      definition: json['definition']?.toString() ?? '',
    );
  }
}

/// A comparison block. The schema for comparisons is loosely typed
/// (varying keys beyond `feature`), so we keep the raw map and expose
/// `feature` plus the remaining key/value pairs generically.
class ComparisonEntry {
  final String feature;
  final Map<String, String> attributes;

  ComparisonEntry({required this.feature, required this.attributes});

  factory ComparisonEntry.fromJson(Map<String, dynamic> json) {
    final attrs = <String, String>{};
    json.forEach((key, value) {
      if (key != 'feature') attrs[key] = value?.toString() ?? '';
    });
    return ComparisonEntry(
      feature: json['feature']?.toString() ?? '',
      attributes: attrs,
    );
  }
}

